void Mult(int (*m)[3][3]){
    int *a=**m;
    int *b=**(m+1);
    for(int (*x)[3]=*(m+2);x<*(m+2)+3;x++){
        for(int *y=*x;y<*(x+1);y++){
            *y=(*a)*(*b);
            a++;
            b+=3;
            *y+=(*a)*(*b);
            a++;
            b+=3;
            *y+=(*a)*(*b);
            std::cout<<*y<<"  ";
            a-=2;
            b-=5;
        }
        std::cout<<std::endl;
        a+=3;
        b=**(m+1);
    }
}
